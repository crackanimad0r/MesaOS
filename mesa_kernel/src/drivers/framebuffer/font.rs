/// Obtiene el bitmap de un carácter
#[inline]
pub fn get_char(c: char) -> &'static [u8; FONT_HEIGHT] {
    let idx = c as usize;
    if idx >= 32 && idx <= 126 {
        &FONT_DATA[idx - 32]
    } else {
        match c {
            'ñ' => &FONT_SPANISH[0],
            'Ñ' => &FONT_SPANISH[1],
            'á' => &FONT_SPANISH[2],
            'é' => &FONT_SPANISH[3],
            'í' => &FONT_SPANISH[4],
            'ó' => &FONT_SPANISH[5],
            'ú' => &FONT_SPANISH[6],
            'Á' => &FONT_SPANISH[7],
            'É' => &FONT_SPANISH[8],
            'Í' => &FONT_SPANISH[9],
            'Ó' => &FONT_SPANISH[10],
            'Ú' => &FONT_SPANISH[11],
            '¿' => &FONT_SPANISH[12],
            '¡' => &FONT_SPANISH[13],
            'º' => &FONT_SPANISH[14],
            'ª' => &FONT_SPANISH[15],
            'ç' => &FONT_SPANISH[16],
            'Ç' => &FONT_SPANISH[17],
            _ => &FONT_DATA[0], // Espacio para caracteres no soportados
        }
    }
}