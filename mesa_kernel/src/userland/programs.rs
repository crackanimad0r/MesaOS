//! Programas embebidos para Ring 3

use alloc::collections::BTreeMap;
use spin::Mutex;

pub static PROGRAMS: Mutex<Option<BTreeMap<&'static str, &'static [u8]>>> = Mutex::new(None);

/// Inicializa los programas disponibles
pub fn init() {
    let mut progs = BTreeMap::new();
    
    // Shell principal
    progs.insert("shell", &SHELL_PROGRAM[..]);
    
    // Programas existentes
    progs.insert("hello", &super::exec::programs::HELLO[..]);
    progs.insert("counter", &super::exec::programs::COUNTER[..]);
    
    // Nuevos programas
    progs.insert("ls", &LS_PROGRAM[..]);
    progs.insert("cat", &CAT_PROGRAM[..]);
    progs.insert("echo", &ECHO_PROGRAM[..]);
    progs.insert("bios", &BIOS_ANALYZE_PROGRAM[..]);
    
    *PROGRAMS.lock() = Some(progs);
    crate::serial_println!("[PROGRAMS] {} programs loaded", progs.len());
}

/// Obtiene el código de un programa
pub fn get_program(name: &str) -> Option<&'static [u8]> {
    PROGRAMS.lock().as_ref()?.get(name).copied()
}

/// Lista los programas disponibles
pub fn list_programs() -> alloc::vec::Vec<&'static str> {
    if let Some(ref progs) = *PROGRAMS.lock() {
        progs.keys().copied().collect()
    } else {
        alloc::vec![]
    }
}

